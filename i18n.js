const translate = (key, language = document.documentElement.lang) =>
  language === "de" ? translations.de[key] : undefined;

const applyLanguage = (language, persist = false) => {
  const selected = language === "de" ? "de" : "en";
  document.documentElement.lang = selected;

  textNodes.forEach((element) => {
    const value = translate(element.dataset.i18n, selected);
    element.textContent = value || originalText.get(element);
  });

  htmlNodes.forEach((element) => {
    const value = translate(element.dataset.i18nHtml, selected);
    element.innerHTML = value || originalHtml.get(element);
  });

  placeholderNodes.forEach((element) => {
    const value = translate(element.dataset.i18nPlaceholder, selected);
    element.setAttribute("placeholder", value || originalPlaceholders.get(element));
  });

  document.title = metadata[selected].title;
  document.querySelector('meta[name="description"]').content = metadata[selected].description;
  document.querySelector('meta[property="og:description"]').content = metadata[selected].ogDescription;
  document.querySelectorAll("[data-language]").forEach((button) => {
    button.setAttribute("aria-pressed", String(button.dataset.language === selected));
  });
  document.querySelector(".language-switch").setAttribute(
    "aria-label",
    selected === "de" ? "Sprachauswahl" : "Language selection"
  );
  document.querySelector(".h-captcha")?.setAttribute("data-lang", selected);

  window.portfolioI18n.language = selected;
  if (persist) {
    try { localStorage.setItem("portfolio-language", selected); } catch (error) { /* Storage may be unavailable. */ }
    const url = new URL(window.location.href);
    url.searchParams.set("lang", selected);
    history.replaceState({}, "", url);
  }
  window.dispatchEvent(new CustomEvent("portfolio-language-change", { detail: { language: selected } }));
};

window.portfolioI18n = { language: "en", translate, applyLanguage };

const queryLanguage = new URLSearchParams(window.location.search).get("lang");
let storedLanguage;
try { storedLanguage = localStorage.getItem("portfolio-language"); } catch (error) { storedLanguage = undefined; }
const browserLanguage = navigator.language?.toLowerCase().startsWith("de") ? "de" : "en";
const initialLanguage = ["en", "de"].includes(queryLanguage)
  ? queryLanguage
  : (["en", "de"].includes(storedLanguage) ? storedLanguage : browserLanguage);

applyLanguage(initialLanguage);
document.querySelectorAll("[data-language]").forEach((button) => {
  button.addEventListener("click", () => applyLanguage(button.dataset.language, true));
});
